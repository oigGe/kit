public class Human {
    //String needs to be spelled with an capital S. The variable "name" should start with a lowercase letter
    String name;
    int age;
    //A single line comment in java starts with two slashes
    int height; //height in centimeter


    //String with capital S. Constructor argument needs data type, in this case int height
    public Human( String name, int age, int height) {
        this.name = name;
        this.age = age;
        this.height = height;
    }}